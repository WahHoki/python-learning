# operasi aritmatika

a = 10
b = 15

# operasi tambah +
hasil = a + b 
print (a,'+',b,'=',hasil)

# operasi pengurangan -
hasil = a - b
print (a,'-',b,'=',hasil)

# operasi kali *
hasil = a * b
print (a,'x',b,'=',hasil)

# operasi bagi /
hasil = a / b
print (a,'/',b,'=',hasil)

# operasi eksponen (pangkat) **
hasil = a ** b
print( a, '**', b, '=', hasil)

# operasi modulus %
hasil = a % b
print( a, '%', b, '=', hasil)

# operasi //
hasil = a // b
print (a,'//',b,'=',hasil)
